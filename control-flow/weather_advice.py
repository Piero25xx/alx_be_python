print ("What's the weather like today? (sunn/rainy/cold): ")

weather_condition = input()

if (weather_condition == 'sunny'):
    print("Wear a t-shirt and sunglasses.")
elif (weather_condition == 'rainy'):
    print("Don't forget your umbrealla and raincoat.")
elif (weather_condition == 'cold'):
    print("Make sure to wear a warm coat and a scarf.")
else :
    print("Sorry, I don't have recommentations for this weather.")

